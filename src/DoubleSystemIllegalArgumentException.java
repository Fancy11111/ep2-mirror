import java.io.IOException;

public class DoubleSystemIllegalArgumentException extends IOException {
    public DoubleSystemIllegalArgumentException() {
        super();
    }

    public DoubleSystemIllegalArgumentException(String message) {
        super(message);
    }

    public DoubleSystemIllegalArgumentException(String message, Throwable cause) {
        super(message, cause);
    }

    public DoubleSystemIllegalArgumentException(Throwable cause) {
        super(cause);
    }
}
