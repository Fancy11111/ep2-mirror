import java.io.IOException;

public class DualSystemIllegalArgumentException extends IOException {
    public DualSystemIllegalArgumentException() {
        super();
    }

    public DualSystemIllegalArgumentException(String message) {
        super(message);
    }

    public DualSystemIllegalArgumentException(String message, Throwable cause) {
        super(message, cause);
    }

    public DualSystemIllegalArgumentException(Throwable cause) {
        super(cause);
    }
}
