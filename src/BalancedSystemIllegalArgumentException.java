import java.io.IOException;

public class BalancedSystemIllegalArgumentException extends IOException {
    public BalancedSystemIllegalArgumentException() {
        super();
    }

    public BalancedSystemIllegalArgumentException(String message) {
        super(message);
    }

    public BalancedSystemIllegalArgumentException(String message, Throwable cause) {
        super(message, cause);
    }

    public BalancedSystemIllegalArgumentException(Throwable cause) {
        super(cause);
    }
}
